// Подклюем пространс имён нашего проекта.
// using — говорит компилятору, где искать наши классы.
using BabayanRandomizer.Модели;
using BabayanRandomizer.Интерфейсы;

// =========================================================
// СОЗДАНИЕ ОБЪЕКТОВ
// =========================================================

Console.OutputEncoding = System.Text.Encoding.UTF8;
Console.WriteLine("=== Рандомайзер ===\n");

// 1. Создаём историю выборов.
var история = new ИсторияВыборов();

// 2. Создаём сервис, передаём ему историю (Dependency Injection).
//    Тип переменной через интерфейс — так код работает с «контрактом»,
//    а не с конкретным классом. Легко заменить реализацию позже.
BabayanRandomizer.Интерфейсы.IСлучайныйВыбор сервис = new РандомайзерСервис(история);

// 3. Создаём список вариантов.
BabayanRandomizer.Интерфейсы.IСписок<Вариант> список =
    new СписокВариантов("Что поесть сегодня?");

// =========================================================
// ПОЛЬЗОВАТЕЛЬСКАЯ ИСТОРИЯ №1: Добавление вариантов
// =========================================================
Console.WriteLine("— Добавляем варианты —");

список.Добавить(new Вариант("Пицца"));
список.Добавить(new Вариант("Суши"));
список.Добавить(new Вариант("Бургер"));
список.Добавить(new Вариант("Шаурма"));
список.Добавить(new Вариант("Паста"));

// Выводим все варианты.
Console.WriteLine($"Всего вариантов: {список.Количество}\n");
foreach (var в in список.ПолучитьВсе())
    Console.WriteLine($"  {в}");

// =========================================================
// ПОЛЬЗОВАТЕЛЬСКАЯ ИСТОРИЯ №3: Редактирование и удаление
// =========================================================
Console.WriteLine("\n— Редактируем и удаляем —");

// Редактируем первый вариант (индекс 0).
список.Редактировать(0, new Вариант("Пицца Маргарита"));
Console.WriteLine("  ✏️  Вариант 0 изменён на «Пицца Маргарита»");

// Удаляем второй вариант (индекс 1 = «Суши»).
список.УдалитьПоИндексу(1);
Console.WriteLine("  🗑️  Вариант «Суши» удалён");

Console.WriteLine($"Вариантов после правок: {список.Количество}");
foreach (var в in список.ПолучитьВсе())
    Console.WriteLine($"  {в}");

// =========================================================
// ПОЛЬЗОВАТЕЛЬСКАЯ ИСТОРИЯ №1,2: Случайный выбор и показ результата
// =========================================================
Console.WriteLine("\n— Делаем случайный выбор —");

var все = список.ПолучитьВсе();
var результат1 = сервис.ВыбратьСлучайно(все);
Console.WriteLine($"\nРезультат: {результат1}");

// =========================================================
// ПОЛЬЗОВАТЕЛЬСКАЯ ИСТОРИЯ №4: Исключение уже выбранных
// =========================================================
Console.WriteLine("\n— Ещё один выбор (без повторов) —");

// Получаем обновлённый список (с учётом флага УжеВыбран).
var все2 = список.ПолучитьВсе();
var результат2 = сервис.ВыбратьСлучайно(все2);
Console.WriteLine($"Результат: {результат2}");

var все3 = список.ПолучитьВсе();
var результат3 = сервис.ВыбратьСлучайно(все3);
Console.WriteLine($"Результат: {результат3}");

// =========================================================
// ПОЛЬЗОВАТЕЛЬСКАЯ ИСТОРИЯ №5: Очистить весь список
// =========================================================
Console.WriteLine("\n— Очищаем список одним нажатием —");
список.ОчиститьВсё();
Console.WriteLine($"Вариантов после очистки: {список.Количество}");

// =========================================================
// ПОКАЗЫВАЕМ ИСТОРИЮ
// =========================================================
Console.WriteLine($"\n— История выборов: {история.КоличествоЗаписей} записи(-ей) —");
foreach (var запись in история.ПолучитьВсю())
    Console.WriteLine($"  {запись}");

Console.WriteLine("\n=== Готово! ===");
