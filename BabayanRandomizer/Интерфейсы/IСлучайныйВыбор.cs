// Второй интерфейс проекта. Описывает контракт для любого
// случайного выбора.
//
// Почему отдельный интерфейс, а не просто класс?
// В будущем можно создать разные реализации:
//   - ОбычныйРандомайзер (использует System.Random)
//   - КриптоРандомайзер (использует более «честный» алгоритм)
//   - ТестовыйРандомайзер (всегда возвращает первый элемент — удобно для тестов)
//
// Код, который ИСПОЛЬЗУЕТ этот интерфейс, не будет знать,
// какая именно реализация используется — он просто вызывает методы.
// ============================================================

using BabayanRandomizer.Модели;

namespace BabayanRandomizer.Интерфейсы
{
    public interface IСлучайныйВыбор
    {
        // Выбрать один случайный вариант из переданного списка.
        // Возвращает объект РезультатВыбора — не сам вариант,
        // а «обёртку» с дополнительной информацией (когда выбрали, что именно).
        РезультатВыбора ВыбратьСлучайно(List<Вариант> варианты);

        // Перегрузка метода — тот же метод, но с параметром исключений.
        // исключённые — список вариантов, которые НЕ должны выпасть.
        // Это реализует пользовательскую историю №4: «исключать уже выбранные».
        РезультатВыбора ВыбратьСлучайно(List<Вариант> варианты, List<Вариант> исключённые);
    }
}
