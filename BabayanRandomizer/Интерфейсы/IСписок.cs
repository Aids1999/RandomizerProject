//
// Зачем нужен интерфейс?
// Представь, что у тебя может быть несколько видов списков:
// обычный список, список с сортировкой, список из базы данных.
// Все они разные внутри, но снаружи ведут себя одинаково.
// Благодаря интерфейсу остальной код может работать
// с ЛЮБЫМ из них, не зная деталей реализации.
// Это называется «полиморфизм».
// ============================================================

namespace BabayanRandomizer.Интерфейсы
{
    // Обобщённый (generic) интерфейс.
    // <T> — это «тип-заглушка». Когда используем
    // этот интерфейс, вместо T подставится конкретный тип,
    // например: IСписок<Вариант> или IСписок<string>.
    public interface IСписок<T>
    {
        // Свойство — это как «умное поле».
        // get; — означает, что значение можно только ЧИТАТЬ снаружи.
        // Возвращает количество элементов в списке.
        int Количество { get; }

        // Метод добавления одного элемента в список.
        // void — означает, что метод ничего не возвращает.
        // элемент — входной параметр метода.
        void Добавить(T элемент);

        // Метод удаления элемента по его индексу (порядковому номеру).
        // Индексы в C# начинаются с 0 (первый элемент = индекс 0).
        void УдалитьПоИндексу(int индекс);

        // Метод редактирования (замены) элемента по индексу.
        // новыйЭлемент — то, на что заменяем.
        void Редактировать(int индекс, T новыйЭлемент);

        // Метод полной очистки списка.
        void ОчиститьВсё();

        // Метод получения элемента по индексу.
        // Возвращает элемент типа T.
        T ПолучитьПоИндексу(int индекс);

        // Метод для получения всех элементов в виде списка.
        // List<T> — стандартная коллекция C# (из пространства имён System.Collections.Generic).
        List<T> ПолучитьВсе();
    }
}
