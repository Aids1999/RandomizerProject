// ============================================================
// ФАЙЛ: Вариант.cs
// ============================================================
// Это базовый КЛАСС доменной модели — «Вариант».
// Он представляет один вариант выбора, который вводит пользователь.
// Например: "Пицца", "Суши", "Бургер" — каждый из них будет
// отдельным объектом моего класса Вариант.
//
// ============================================================

namespace BabayanRandomizer.Модели
{
    // "public" — означает, что класс доступен из других файлов/пространств имён.
    public class Вариант
    {
        // -------------------------------------------------------
        // ПОЛЯ И СВОЙСТВА
        // -------------------------------------------------------

        // Guid — специальный тип в C# для генерации уникальных ID.
        // Например: "3f2504e0-4f66-66d3-9a0c-0305e82c3301"
        // Это гарантирует, что два варианта с одинаковым текстом
        // всё равно будут разными объектами.
        public Guid Идентификатор { get; private set; }

        // Текст варианта, который вводит пользователь.
        // string — тип данных для текста в C#.
        // get; set; — свойство можно и читать, и записывать.
        public string Текст { get; set; }

        // Флаг: был ли этот вариант уже выбран ранее?
        // bool — булевый тип: true (правда) или false (ложь)
        // Используется для пользовательской истории 4
        public bool УжеВыбран { get; set; }

        // Дата и время, когда вариант был добавлен в список.
        // DateTime — тип данных для хранения даты и времени.
        public DateTime ДатаДобавления { get; private set; }

        // -------------------------------------------------------
        // КОНСТРУКТОР
        // -------------------------------------------------------
        // Конструктор — это специальный метод, который вызывается
        // при создании нового объекта: new Вариант("Пицца").
        // Он «настраивает» объект при его рождении.
        public Вариант(string текст)
        {
            // Проверка: нельзя создать вариант с пустым текстом.
            // string.IsNullOrWhiteSpace — проверяет, что строка
            // не равна null
            if (string.IsNullOrWhiteSpace(текст))
                throw new ArgumentException("Эй! текст варианта не может быть пустым!", nameof(текст));

            // Guid.NewGuid() — генерирует новый уникальный идентификатор.
            Идентификатор = Guid.NewGuid();

            // .Trim() — убирает лишние пробелы в начале и конце строки.
            Текст = текст.Trim();

            // По умолчанию вариант ещё не был выбран.
            УжеВыбран = false;

            // DateTime.Now — текущие дата и время.
            ДатаДобавления = DateTime.Now;
        }

        // -------------------------------------------------------
        // МЕТОДЫ
        // -------------------------------------------------------

        // Метод ОтметитьКакВыбранный — меняет флаг на true.
        // Вызывается после того, как этот вариант выпал в рандомайзере.
        public void ОтметитьКакВыбранный()
        {
            УжеВыбран = true;
        }

        // Метод СброситьВыбор — возвращает флаг в исходное состояние.
        // Нужен, если пользователь хочет, чтобы вариант мог выпасть снова.
        public void СброситьВыбор()
        {
            УжеВыбран = false;
        }

        // override ToString() — переопределение стандартного метода.
        // В C# каждый объект «умеет» преобразовываться в строку через .ToString().
        // По умолчанию выводится имя класса. Мы переопределяем это,
        // чтобы выводился текст варианта. Удобно при отладке.
        public override string ToString()
        {
            // Интерполяция строк: $"текст {переменная}" — удобный способ
            // вставить значение переменной внутрь строки.
            return $"[{(УжеВыбран ? "v" : " ")}] {Текст}";
        }
    }
}
