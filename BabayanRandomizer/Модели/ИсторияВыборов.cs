// Этот класс хранит ИСТОРИЮ всех сделанных случайных выборов.
// Это пример паттерна «Журнал» или «Log».
//
// Почему это отдельный класс, а не список в рандомайзере?
// Принцип единственной ответственности (Single Responsibility Principle):
// каждый класс должен делать ОДНО дело.
//   - РандомайзерСервис отвечает за выбор.
//   - ИсторияВыборов отвечает за хранение истории.
// Так проще изменять, тестировать и читать код.
//
// АГРЕГАЦИЯ: ИсторияВыборов содержит список объектов РезультатВыбора.
// ============================================================

namespace BabayanRandomizer.Модели
{
    public class ИсторияВыборов
    {
        // Приватный список для хранения всех результатов.
        private readonly List<РезультатВыбора> _записи;

        // Количество записей в истории.
        public int КоличествоЗаписей => _записи.Count;

        // Конструктор инициализирует пустой список.
        public ИсторияВыборов()
        {
            _записи = new List<РезультатВыбора>();
        }

        // Добавить новый результат выбора в историю.
        public void ДобавитьЗапись(РезультатВыбора результат)
        {
            if (результат == null)
                throw new ArgumentNullException(nameof(результат));

            _записи.Add(результат);
        }

        // Получить полную историю (копию списка).
        public List<РезультатВыбора> ПолучитьВсю()
        {
            return new List<РезультатВыбора>(_записи);
        }

        // Получить последний результат или null, если история пуста.
        // LastOrDefault() — LINQ-метод: возвращает последний элемент
        // или значение по умолчанию (null для ссылочных типов).
        public РезультатВыбора? ПолучитьПоследний()
        {
            return _записи.LastOrDefault();
        }

        // Очистить всю историю.
        public void Очистить()
        {
            _записи.Clear();
        }

        // Получить только успешные выборы.
        // Где Успешен == true.
        public List<РезультатВыбора> ПолучитьУспешные()
        {
            return _записи.Where(r => r.Успешен).ToList();
        }

        public override string ToString()
        {
            return $"История выборов: {КоличествоЗаписей} записей";
        }
    }
}
