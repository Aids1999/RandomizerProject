// ============================================================
// ФАЙЛ: РезультатВыбора.cs
// ============================================================
// Этот класс представляет РЕЗУЛЬТАТ одного случайного выбора.
// Почему не просто вернуть объект Вариант?
//
// Потому что результат содержит ДОПОЛНИТЕЛЬНЫЙ контекст:
//   - когда именно произошёл выбор,
//   - из скольких вариантов выбирали,
//   - был ли выбор вообще возможен (или все варианты исключены).
//
// Это паттерн «Value Object» (Объект-значение) из Domain-Driven Design.
// Такой объект не изменяется после создания — он только читается.
// ============================================================

namespace BabayanRandomizer.Модели
{
    public class РезультатВыбора
    {
        // -------------------------------------------------------
        // СВОЙСТВА (все только для чтения — private set)
        // -------------------------------------------------------

        // Выбранный вариант. Может быть null, если выбор невозможен
        // (например, все варианты уже были выбраны).
        // Вариант? — знак «?» означает «nullable» тип:
        // переменная может содержать null (отсутствие значения).
        public Вариант? ВыбранныйВариант { get; private set; }

        // Дата и время, когда произошёл выбор.
        public DateTime ВремяВыбора { get; private set; }

        // Сколько вариантов было доступно для выбора
        // (не считая исключённых).
        public int КоличествоДоступных { get; private set; }

        // Флаг: успешен ли выбор? Если список пуст или все исключены — false.
        public bool Успешен { get; private set; }

        // Сообщение для пользователя — описание результата.
        public string Сообщение { get; private set; }

        // -------------------------------------------------------
        // КОНСТРУКТОРЫ
        // -------------------------------------------------------

        // Конструктор успешного результата.
        // можно иметь НЕСКОЛЬКО конструкторов с разными параметрами —
        // это называется «перегрузка конструктора».
        public РезультатВыбора(Вариант выбранный, int количествоДоступных)
        {
            ВыбранныйВариант = выбранный;
            КоличествоДоступных = количествоДоступных;
            ВремяВыбора = DateTime.Now;
            Успешен = true;
            Сообщение = $"Выбран: «{выбранный.Текст}»";
        }

        // Конструктор неудачного результата (когда выбрать нельзя).
        // string сообщение — пояснение, почему выбор невозможен.
        public РезультатВыбора(string сообщение)
        {
            ВыбранныйВариант = null;
            КоличествоДоступных = 0;
            ВремяВыбора = DateTime.Now;
            Успешен = false;
            Сообщение = сообщение;
        }

        // -------------------------------------------------------
        // МЕТОДЫ
        // -------------------------------------------------------

        public override string ToString()
        {
            if (Успешен)
                return $"V {Сообщение} (из {КоличествоДоступных} вариантов, в {ВремяВыбора:HH:mm:ss})";
            else
                return $"X {Сообщение}";
        }
    }
}
