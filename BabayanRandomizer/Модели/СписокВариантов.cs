// ============================================================
// ФАЙЛ: СписокВариантов.cs
// ============================================================
// Это АГРЕГАТ (Aggregate) — центральный класс доменной модели.

// Агрегат — это «корень» группы связанных объектов.
// Он управляет ими и обеспечивает их целостность.
// Снаружи мы работаем только с агрегатом, а не с объектами напрямую.
//
// В нашем случае: СписокВариантов управляет множеством объектов Вариант.
// Все операции (добавить, удалить, редактировать) проходят ЧЕРЕЗ него.
//
// РЕАЛИЗУЕТ интерфейс IСписок<Вариант>.
// Знак ":" после имени класса означает реализацию интерфейса или наследование.
//
// АГРЕГАЦИЯ (vs. Компоновка):
// СписокВариантов АГРЕГИРУЕТ (содержит) объекты Вариант.
// Если список удалить, варианты технически могут существовать отдельно.
// Это отличает агрегацию от компоновки (где содержимое не живёт без контейнера).
// ============================================================

using BabayanRandomizer.Интерфейсы;

namespace BabayanRandomizer.Модели
{
    // Ключевое слово "sealed" означает: от этого класса НЕЛЬЗЯ наследоваться.
    // Это дизайнерское решение: мы не хотим, чтобы кто-то создавал
    // "изменённые" версии списка — лучше создать новый через интерфейс.
    public sealed class СписокВариантов : IСписок<Вариант>
    {
        // -------------------------------------------------------
        // ПРИВАТНЫЕ ПОЛЯ
        // -------------------------------------------------------
        // private — поле доступно только ВНУТРИ этого класса.
        // List<Вариант> — стандартная коллекция .NET.
        // readonly — поле нельзя переприсвоить после создания объекта,
        //            но содержимое списка менять можно.
        private readonly List<Вариант> _варианты;

        // -------------------------------------------------------
        // ПУБЛИЧНЫЕ СВОЙСТВА
        // -------------------------------------------------------

        // Название категории списка (например: "Еда", "Фильмы").
        // Это делает список именованным и удобным для отображения.
        public string Название { get; set; }

        // Реализация свойства из интерфейса IСписок<Вариант>.
        // Count — стандартное свойство List<T>, возвращает количество элементов.
        public int Количество => _варианты.Count;

        // Вычисляемое свойство: количество НЕ выбранных вариантов.
        // => — это «expression body»: сокращённый способ записи get { return ...; }
        // .Count(v => !v.УжеВыбран) — LINQ-запрос: считаем элементы, где УжеВыбран = false.
        // LINQ — это встроенный язык запросов C# для работы с коллекциями.
        public int КоличествоДоступных => _варианты.Count(v => !v.УжеВыбран);

        // Вычисляемое свойство: true если список пустой.
        public bool Пустой => _варианты.Count == 0;

        // -------------------------------------------------------
        // КОНСТРУКТОР
        // -------------------------------------------------------
        public СписокВариантов(string название = "Мой список")
        {
            // string название = "Мой список" — параметр со значением по умолчанию.
            // Можно создать список без аргументов: new СписокВариантов()
            // Или с именем: new СписокВариантов("Фильмы")

            if (string.IsNullOrWhiteSpace(название))
                название = "Без названия";

            Название = название.Trim();
            _варианты = new List<Вариант>();
        }

        // -------------------------------------------------------
        // МЕТОДЫ ИНТЕРФЕЙСА IСписок<Вариант>
        // -------------------------------------------------------

        // Добавляем вариант в конец списка.
        // Проверяем дубликаты: нельзя добавить два одинаковых текста.
        public void Добавить(Вариант элемент)
        {
            if (элемент == null)
                throw new ArgumentNullException(nameof(элемент), "Вариант не может быть null.");

            // Any() — LINQ-метод: возвращает true, если хоть один элемент удовлетворяет условию.
            // StringComparison.OrdinalIgnoreCase — сравниваем без учёта регистра ("пицца" = "Пицца").
            bool дублируется = _варианты.Any(v =>
                string.Equals(v.Текст, элемент.Текст, StringComparison.OrdinalIgnoreCase));

            if (дублируется)
                throw new InvalidOperationException($"Вариант «{элемент.Текст}» уже есть в списке.");

            _варианты.Add(элемент);
        }

        // Удаляем по индексу. Проверяем, что индекс корректный.
        public void УдалитьПоИндексу(int индекс)
        {
            ПроверитьИндекс(индекс);
            _варианты.RemoveAt(индекс);
        }

        // Редактируем текст варианта по индексу.
        // Изменяем свойство существующего объекта, не заменяем сам объект.
        public void Редактировать(int индекс, Вариант новый)
        {
            ПроверитьИндекс(индекс);
            if (новый == null)
                throw new ArgumentNullException(nameof(новый));

            _варианты[индекс].Текст = новый.Текст;
        }

        // Очищаем весь список одним методом (пользовательская история №5).
        public void ОчиститьВсё()
        {
            _варианты.Clear();
        }

        // Получить конкретный вариант по индексу.
        public Вариант ПолучитьПоИндексу(int индекс)
        {
            ПроверитьИндекс(индекс);
            return _варианты[индекс];
        }

        // Вернуть копию списка всех вариантов.
        // Именно КОПИЮ — чтобы внешний код не мог напрямую изменить внутренний список.
        // new List<Вариант>(_варианты) — конструктор копирования.
        public List<Вариант> ПолучитьВсе()
        {
            return new List<Вариант>(_варианты);
        }

        // -------------------------------------------------------
        // ДОПОЛНИТЕЛЬНЫЕ МЕТОДЫ (не из интерфейса)
        // -------------------------------------------------------

        // Получить только те варианты, которые ещё не были выбраны.
        // Where() — LINQ-метод фильтрации. Возвращает только элементы,
        // удовлетворяющие условию.
        public List<Вариант> ПолучитьДоступные()
        {
            return _варианты.Where(v => !v.УжеВыбран).ToList();
        }

        // Сбросить отметку «уже выбран» у всех вариантов.
        // Полезно, если пользователь хочет начать рандомизацию заново.
        public void СброситьВсеВыборы()
        {
            // foreach — цикл, проходящий по каждому элементу коллекции.
            foreach (var вариант in _варианты)
            {
                вариант.СброситьВыбор();
            }
        }

        // -------------------------------------------------------
        // ПРИВАТНЫЕ ВСПОМОГАТЕЛЬНЫЕ МЕТОДЫ
        // -------------------------------------------------------

        // Общая проверка индекса. Вызывается из разных методов.
        // Инкапсуляция: выносим повторяющийся код в отдельный метод.
        private void ПроверитьИндекс(int индекс)
        {
            // Допустимый диапазон: от 0 до (Количество - 1).
            if (индекс < 0 || индекс >= _варианты.Count)
                throw new ArgumentOutOfRangeException(nameof(индекс),
                    $"Индекс {индекс} выходит за пределы списка (0–{_варианты.Count - 1}).");
        }

        public override string ToString()
        {
            return $"Список «{Название}»: {Количество} вар. ({КоличествоДоступных} доступных)";
        }
    }
}
