// ФАЙЛ: РандомайзерСервис.cs
// ============================================================
// Это СЕРВИСНЫЙ КЛАСС — он содержит логику, которая не принадлежит
// ни одной отдельной сущности, а «живёт» между ними.
//
// Конкретно: класс реализует интерфейс IСлучайныйВыбор.
// Таким образом, он отвечает за «умение делать случайный выбор».
//
// ПОЧЕМУ ОТДЕЛЬНЫЙ КЛАСС?
// Можно было бы добавить метод ВыбратьСлучайно() прямо в СписокВариантов.
// Но тогда класс нарушал бы принцип единственной ответственности:
// он и хранил бы данные, И реализовывал логику выбора.
// Разделение упрощает замену алгоритма без изменения модели данных.
//
// РЕАЛИЗУЕТ: IСлучайныйВыбор
// ИСПОЛЬЗУЕТ (ЗАВИСИТ ОТ): ИсторияВыборов (агрегация через конструктор)
// ============================================================

using BabayanRandomizer.Интерфейсы;

namespace BabayanRandomizer.Модели
{
    public class РандомайзерСервис : IСлучайныйВыбор
    {
        // -------------------------------------------------------
        // ЗАВИСИМОСТИ (Dependencies)
        // -------------------------------------------------------
        // Сервис «знает» об истории выборов и записывает в неё результаты.
        // Это пример «агрегации»: РандомайзерСервис использует ИсторияВыборов,
        // но не создаёт её — она передаётся снаружи (Dependency Injection).
        private readonly ИсторияВыборов _история;

        // Random — стандартный класс C# для генерации псевдослучайных чисел.
        private readonly Random _генератор;

        // -------------------------------------------------------
        // КОНСТРУКТОР
        // -------------------------------------------------------
        // история — объект истории, передаётся извне (инверсия зависимостей).
        public РандомайзерСервис(ИсторияВыборов история)
        {
            _история = история ?? throw new ArgumentNullException(nameof(история));
            // new Random() — создаём генератор псевдослучайных чисел.
            // Он автоматически инициализируется текущим временем как «семенем».
            _генератор = new Random();
        }

        // -------------------------------------------------------
        // РЕАЛИЗАЦИЯ ИНТЕРФЕЙСА
        // -------------------------------------------------------

        // Реализация первого метода интерфейса IСлучайныйВыбор.
        // Выбирает случайный вариант из переданного списка (без исключений).
        public РезультатВыбора ВыбратьСлучайно(List<Вариант> варианты)
        {
            // Вызываем перегрузку с пустым списком исключений.
            // new List<Вариант>() — пустой список типа Вариант.
            return ВыбратьСлучайно(варианты, new List<Вариант>());
        }

        // Реализация второго метода интерфейса — с поддержкой исключений.
        // Это реализует пользовательскую историю №4.
        public РезультатВыбора ВыбратьСлучайно(List<Вариант> варианты, List<Вариант> исключённые)
        {
            РезультатВыбора результат;

            // Проверяем: есть ли вообще варианты?
            if (варианты == null || варианты.Count == 0)
            {
                результат = new РезультатВыбора("Список вариантов пуст. Добавьте хотя бы один вариант.");
            }
            else
            {
                // Получаем список идентификаторов исключённых вариантов.
                // .Select() — LINQ: преобразуем каждый элемент.
                // .ToHashSet() — создаём HashSet (множество) для быстрой проверки «входит ли элемент?»
                var исключённыеId = исключённые?
                    .Select(v => v.Идентификатор)
                    .ToHashSet() ?? new HashSet<Guid>();

                // Фильтруем: берём только те варианты, которых нет в исключённых
                // и которые ещё не были выбраны.
                var доступные = варианты
                    .Where(v => !исключённыеId.Contains(v.Идентификатор) && !v.УжеВыбран)
                    .ToList();

                if (доступные.Count == 0)
                {
                    результат = new РезультатВыбора(
                        "Все варианты уже были выбраны/исключены. Сбросьте выборы или добавьте новые.");
                }
                else
                {
                    // Случайный индекс: от 0 до (доступные.Count - 1).
                    // _генератор.Next(min, max) возвращает число в диапазоне [min, max).
                    int случайныйИндекс = _генератор.Next(0, доступные.Count);
                    var выбранный = доступные[случайныйИндекс];

                    // Отмечаем вариант как «уже выбранный».
                    выбранный.ОтметитьКакВыбранный();

                    результат = new РезультатВыбора(выбранный, доступные.Count);
                }
            }

            // Записываем результат в историю (независимо от успеха).
            _история.ДобавитьЗапись(результат);

            return результат;
        }
    }
}
